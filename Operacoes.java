public class Operacoes {
    public float soma (float a, float b){
        return a+b;
    }
    public float subtracao (float a, float b){
        return a-b;
    }
    public float multiplicacao (float a, float b){
        return a*b;
    }
    public float divisao (float a, float b){
        return a/b;
    }
}